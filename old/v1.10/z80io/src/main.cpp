#include <Arduino.h>
#include <bus_access.h>

bool g_Verbose = true;

const byte os_data[] =
{
	0x00, 0x00, 0x00, 0x00, 0x00, 0x21, 0x11, 0x00, 0x06, 0x0D, 0x0E, 0x00, 0xED, 0xB3, 0xC3, 0x00,
	0x00, 0x48, 0x65, 0x6C, 0x6C, 0x6F, 0x20, 0x57, 0x6F, 0x72, 0x6C, 0x64, 0x0D, 0x0A, 0x00, 0x00
} ;

void setup()
{
	Serial.begin(115200);
	for (int i = 10; i <= 0;--i)
	{
		delay(1000);
		Serial.println(i);
	}
	bus_setup();
	upload(os_data, 0, sizeof(os_data));
}

void loop()
{
	bus_loop();
}

